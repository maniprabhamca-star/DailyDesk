#!/usr/bin/env node
// Generates the two status artifacts (docs/artifacts/overview-status-board.html
// and overview-product.html) DIRECTLY from the tool catalog, so their counts and
// tool lists can never drift from the site again. Re-run after every tool ship:
//
//   node frontend/scripts/gen-status-artifacts.mjs
//
// It parses components/app/catalog.tsx as text (the entries are uniform), so it
// needs no build step / no React runtime. Counts match `liveToolCount` (a tool is
// "live" when it has an href AND is not flagged soon:true).
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..', '..');
const CATALOG = join(ROOT, 'frontend', 'components', 'app', 'catalog.tsx');
const OUT_DIR = join(ROOT, 'docs', 'artifacts');

// --- parse the catalog ------------------------------------------------------
const src = readFileSync(CATALOG, 'utf8');

// Grab everything inside `export const catalog: CatGroup[] = [ ... ];`
const catBody = src.slice(src.indexOf('export const catalog'));
const groupRe = /label:\s*'([^']+)',\s*color:\s*'([^']+)',\s*tools:\s*\[([\s\S]*?)\]\s*,?\s*\}/g;
const toolRe = /\{\s*name:\s*'([^']+)'([\s\S]*?)\}/g;

const groups = [];
let gm;
while ((gm = groupRe.exec(catBody))) {
  const [, label, color, toolsBlock] = gm;
  const tools = [];
  let tm;
  while ((tm = toolRe.exec(toolsBlock))) {
    const [, name, rest] = tm;
    const href = (rest.match(/href:\s*'([^']+)'/) || [])[1] || null;
    const badge = (rest.match(/badge:\s*'([^']+)'/) || [])[1] || 'device';
    const soon = /\bsoon:\s*true/.test(rest);
    tools.push({ name, href, badge, soon, live: !!href && !soon });
  }
  groups.push({ label, color, tools });
}

if (!groups.length) {
  console.error('gen-status-artifacts: parsed 0 groups — catalog format changed? Aborting.');
  process.exit(1);
}

const all = groups.flatMap((g) => g.tools);
const live = all.filter((t) => t.live).length;
const total = all.length;
const onDevice = all.filter((t) => t.badge === 'device' && t.live).length;
const serverAi = live - onDevice;
const soonCount = all.filter((t) => t.soon).length;
const today = new Date().toISOString().slice(0, 10);

console.log(`Parsed ${groups.length} groups, ${total} tools, ${live} live (${onDevice} on-device, ${serverAi} server/AI), ${soonCount} coming soon.`);

// --- shared HTML bits -------------------------------------------------------
const esc = (s) => String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
const AUTOGEN = `<!-- AUTO-GENERATED by frontend/scripts/gen-status-artifacts.mjs on ${today} — do not hand-edit; re-run the script. Source of truth: components/app/catalog.tsx -->`;

const SHELL = (title, body) => `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${esc(title)}</title>
${AUTOGEN}
<style>
  :root { color-scheme: light dark; --bg:#f7f7fb; --card:#fff; --ink:#14132e; --muted:#5b5b74; --line:#e6e6ef; --accent:#4f46e5; }
  @media (prefers-color-scheme: dark){ :root{ --bg:#0a0a18; --card:#14132e; --ink:#ececff; --muted:#a3a3c2; --line:#26264a; --accent:#8b87ff; } }
  *{box-sizing:border-box} body{margin:0;background:var(--bg);color:var(--ink);font:15px/1.5 -apple-system,BlinkMacSystemFont,'Segoe UI',Roboto,sans-serif}
  .wrap{max-width:1100px;margin:0 auto;padding:40px 20px}
  h1{font-size:30px;margin:0 0 4px} .sub{color:var(--muted);margin:0 0 28px}
  .stats{display:flex;flex-wrap:wrap;gap:26px;margin:0 0 34px}
  .stat .n{font-size:34px;font-weight:800;line-height:1} .stat .l{color:var(--muted);font-size:13px;margin-top:4px}
  .grid{display:grid;grid-template-columns:repeat(auto-fill,minmax(320px,1fr));gap:16px}
  .card{background:var(--card);border:1px solid var(--line);border-radius:14px;padding:16px 18px}
  .card h2{font-size:16px;margin:0 0 10px;display:flex;align-items:center;gap:8px}
  .dot{width:10px;height:10px;border-radius:50%}
  .cnt{margin-left:auto;font-size:12px;color:var(--muted);font-weight:600}
  ul{list-style:none;margin:0;padding:0}
  li{display:flex;align-items:center;gap:8px;padding:3px 0;font-size:14px}
  .b{font-size:10px;font-weight:700;text-transform:uppercase;letter-spacing:.03em;padding:1px 6px;border-radius:6px;margin-left:auto}
  .b-soon{background:rgba(217,119,6,.14);color:#d97706} .b-srv{background:rgba(217,119,6,.1);color:#b45309}
  .b-ai{background:rgba(124,58,237,.14);color:#7c3aed} .b-enc{background:rgba(37,99,235,.14);color:#2563eb} .b-acc{background:rgba(8,145,178,.14);color:#0891b2}
  .foot{margin-top:34px;color:var(--muted);font-size:12px;border-top:1px solid var(--line);padding-top:16px}
  a{color:var(--accent)}
</style>
</head>
<body><div class="wrap">${body}</div></body>
</html>`;

const badgeTag = (t) => {
  if (t.soon) return '<span class="b b-soon">Soon</span>';
  if (t.badge === 'server') return '<span class="b b-srv">Server</span>';
  if (t.badge === 'ai') return '<span class="b b-ai">AI</span>';
  if (t.badge === 'encrypted') return '<span class="b b-enc">E2E</span>';
  if (t.badge === 'account') return '<span class="b b-acc">Account</span>';
  return '';
};

const statBlock = (pairs) => `<div class="stats">${pairs
  .map(([n, l]) => `<div class="stat"><div class="n">${n}</div><div class="l">${esc(l)}</div></div>`)
  .join('')}</div>`;

// --- artifact 1: status board ----------------------------------------------
const boardCards = groups
  .map((g) => {
    const gl = g.tools.filter((t) => t.live).length;
    const items = g.tools
      .map((t) => `<li>${t.live ? '✅' : '🕓'} ${esc(t.name)}${badgeTag(t)}</li>`)
      .join('');
    return `<div class="card"><h2><span class="dot" style="background:${g.color}"></span>${esc(g.label)}<span class="cnt">${gl}/${g.tools.length} live</span></h2><ul>${items}</ul></div>`;
  })
  .join('');

// Pro roadmap — TIERED + revenue-safe. Curated here (NOT in the catalog, since
// these are plans, not shipped tools). Strategy: deterministic features = zero
// marginal cost (pure margin); server/AI features must be metered + capped so
// cost never exceeds revenue. Status: 🌓 built-dark (owner-only until launch) ·
// ◐ partial · ⏳ planned.
const PRO_ROADMAP = [
  { tier: 'Tier A — Deterministic (zero cost · pure margin)', color: '#16a34a', items: [
    ['On-device batch — 100 files, zero uploads', 'partial'],
    ['Saved workflows + presets', 'planned'],
    ['Self-destruct encrypted shares', 'planned'],
    ['Redaction / clean certificate', 'planned'],
    ['RFC-3161 trusted timestamp', 'planned'],
    ['Brand kit (logo / watermark presets)', 'planned'],
  ] },
  { tier: 'Tier B — Server / AI (metered + capped)', color: '#7c3aed', items: [
    ['AI document suite (8 tools)', 'dark'],
    ['Encrypted File Vault', 'dark'],
    ['Link in Bio', 'dark'],
    ['Receipt Scanner', 'dark'],
    ['Bank Statement Converter (flagship)', 'partial'],
    ['Encrypted "send large file"', 'planned'],
    ['PDF → Audio MP3 export', 'planned'],
    ['e-Invoice (Factur-X / ZUGFeRD + GST)', 'planned'],
  ] },
  { tier: 'Tier C — Table-stakes Pro', color: '#0891b2', items: [
    ['OCR', 'dark'],
    ['Bigger server-tool files', 'planned'],
    ['Unlimited server conversions', 'planned'],
    ['Cloud import (Drive / Dropbox)', 'planned'],
    ['Larger Vault tiers', 'planned'],
    ['Priority processing', 'planned'],
  ] },
];
const stMark = (s) => (s === 'dark' ? '🌓' : s === 'partial' ? '◐' : '⏳');
const proSection = `<h2 style="font-size:20px;margin:40px 0 4px">Pro roadmap — tiered &amp; revenue-safe</h2>
<p class="sub" style="margin-bottom:18px">🌓 built, owner-only until launch · ◐ partly built · ⏳ planned. Deterministic features cost $0 to serve (pure margin); server/AI features are metered so cost never exceeds revenue.</p>
<div class="grid">${PRO_ROADMAP.map((t) => `<div class="card"><h2><span class="dot" style="background:${t.color}"></span>${esc(t.tier)}</h2><ul>${t.items.map(([n, s]) => `<li>${stMark(s)} ${esc(n)}</li>`).join('')}</ul></div>`).join('')}</div>`;

const board = SHELL(
  'DiemDesk — Status Board',
  `<h1>DiemDesk status board</h1>
<p class="sub">Auto-generated from the live catalog · ${today}</p>
${statBlock([
    [live, 'Live & public'],
    [onDevice, 'Run on your device'],
    [serverAi, 'Server / AI / synced'],
    [soonCount, 'Coming soon'],
    [total, 'Total in catalog'],
  ])}
<div class="grid">${boardCards}</div>
${proSection}
<p class="foot">Source of truth: <code>frontend/components/app/catalog.tsx</code> (live tools) + the Pro roadmap curated in <code>gen-status-artifacts.mjs</code>. The live site's <a href="https://diemdesk.com/overview">/overview</a> shows only shipped tools. Regenerate with <code>node frontend/scripts/gen-status-artifacts.mjs</code>.</p>`
);

// --- artifact 2: product overview (prose + category counts) -----------------
const prodRows = groups
  .map((g) => {
    const gl = g.tools.filter((t) => t.live).length;
    return `<div class="card"><h2><span class="dot" style="background:${g.color}"></span>${esc(g.label)}<span class="cnt">${gl} live</span></h2><ul>${g.tools
      .filter((t) => t.live)
      .map((t) => `<li>${esc(t.name)}${badgeTag(t)}</li>`)
      .join('')}</ul></div>`;
  })
  .join('');

const product = SHELL(
  'DiemDesk — Product Overview',
  `<h1>DiemDesk</h1>
<p class="sub">${live} live tools across ${groups.length} categories · ${onDevice} run entirely on your device · ${today}</p>
${statBlock([
    [live, 'Live tools'],
    [onDevice, 'On-device (zero upload)'],
    [serverAi, 'Server / AI / synced'],
    [groups.length, 'Categories'],
  ])}
<div class="grid">${prodRows}</div>
<p class="foot">Auto-generated from <code>catalog.tsx</code>. See <a href="https://diemdesk.com/overview">diemdesk.com/overview</a> for the authoritative, always-current view.</p>`
);

writeFileSync(join(OUT_DIR, 'overview-status-board.html'), board);
writeFileSync(join(OUT_DIR, 'overview-product.html'), product);
console.log('Wrote docs/artifacts/overview-status-board.html + overview-product.html');
